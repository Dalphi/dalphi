require 'rails_helper'

RSpec.describe "services/edit", type: :view do
  before(:each) do
    @service = assign(:service, Service.create!(
      :roll => 1,
      :description => "MyString",
      :capability => 1,
      :url => "MyString",
      :title => "MyString",
      :version => "MyString"
    ))
  end

  it "renders the edit service form" do
    render

    assert_select "form[action=?][method=?]", service_path(@service), "post" do

      assert_select "input#service_roll[name=?]", "service[roll]"

      assert_select "input#service_description[name=?]", "service[description]"

      assert_select "input#service_capability[name=?]", "service[capability]"

      assert_select "input#service_url[name=?]", "service[url]"

      assert_select "input#service_title[name=?]", "service[title]"

      assert_select "input#service_version[name=?]", "service[version]"
    end
  end
end
